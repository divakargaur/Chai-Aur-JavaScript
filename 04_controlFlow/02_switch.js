const month = 3 // "march"
switch (month) { // case sensitive
    case 1: // "january"
        console.log("January");
        break;
    case 2: // "february"
        console.log("February");
        break;
    case 3: // "march"
        console.log("March");
        break;
    case 4: // "april"
        console.log("April");
        break;

    default:
        console.log("DEFAULT");
        break;
}